package com.everhomes.parking.dashi;

/**
 * @author sw on 2017/8/22.
 */
public class DashiEmptyPlaceFloorInfo {
    private String FloorAddress;
    private String ParkingTotal;
    private String ParkingVacancy;

    public String getFloorAddress() {
        return FloorAddress;
    }

    public void setFloorAddress(String floorAddress) {
        FloorAddress = floorAddress;
    }

    public String getParkingTotal() {
        return ParkingTotal;
    }

    public void setParkingTotal(String parkingTotal) {
        ParkingTotal = parkingTotal;
    }

    public String getParkingVacancy() {
        return ParkingVacancy;
    }

    public void setParkingVacancy(String parkingVacancy) {
        ParkingVacancy = parkingVacancy;
    }
}
