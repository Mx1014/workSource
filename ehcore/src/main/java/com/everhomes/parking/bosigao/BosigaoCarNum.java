package com.everhomes.parking.bosigao;

public class BosigaoCarNum {
	private String ParkingID;
	private String ParkName;
	private Integer CarNum;
	private Integer SpaceNum;
	private Integer InCarNum;
	
	public String getParkingID() {
		return ParkingID;
	}
	public void setParkingID(String parkingID) {
		ParkingID = parkingID;
	}
	public String getParkName() {
		return ParkName;
	}
	public void setParkName(String parkName) {
		ParkName = parkName;
	}
	public Integer getCarNum() {
		return CarNum;
	}
	public void setCarNum(Integer carNum) {
		CarNum = carNum;
	}
	public Integer getSpaceNum() {
		return SpaceNum;
	}
	public void setSpaceNum(Integer spaceNum) {
		SpaceNum = spaceNum;
	}
	public Integer getInCarNum() {
		return InCarNum;
	}
	public void setInCarNum(Integer inCarNum) {
		InCarNum = inCarNum;
	}
}
