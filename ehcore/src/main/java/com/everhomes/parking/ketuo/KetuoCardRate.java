package com.everhomes.parking.ketuo;

public class KetuoCardRate {
	private String carType;
	private String typeName;
	private String ruleId;
	private String ruleName;
	private String ruleType;
	private String ruleAmount;
	private String ruleMoney;
	
	public String getRuleId() {
		return ruleId;
	}
	public void setRuleId(String ruleId) {
		this.ruleId = ruleId;
	}
	public String getRuleName() {
		return ruleName;
	}
	public void setRuleName(String ruleName) {
		this.ruleName = ruleName;
	}
	public String getRuleType() {
		return ruleType;
	}
	public void setRuleType(String ruleType) {
		this.ruleType = ruleType;
	}
	public String getRuleAmount() {
		return ruleAmount;
	}
	public void setRuleAmount(String ruleAmount) {
		this.ruleAmount = ruleAmount;
	}
	public String getRuleMoney() {
		return ruleMoney;
	}
	public void setRuleMoney(String ruleMoney) {
		this.ruleMoney = ruleMoney;
	}
	public String getCarType() {
		return carType;
	}
	public void setCarType(String carType) {
		this.carType = carType;
	}
	public String getTypeName() {
		return typeName;
	}
	public void setTypeName(String typeName) {
		this.typeName = typeName;
	}
	
}
